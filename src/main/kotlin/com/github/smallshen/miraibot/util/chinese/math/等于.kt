infix fun Int.等于(value: Int): Boolean {
    return this == value
}


infix fun Long.等于(value: Long): Boolean {
    return this == value
}


infix fun Float.等于(value: Float): Boolean {
    return this == value
}


infix fun Double.等于(value: Double): Boolean {
    return this == value
}

infix fun String?.等于(value: String?) : Boolean {
    return this == value
}

